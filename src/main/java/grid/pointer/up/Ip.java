package grid.pointer.up;

public class Ip {

	String id;
	String ip;
	
	public Ip(String id, String ip) {
		super();
		this.id = id;
		this.ip = ip;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getIp() {
		return ip;
	}
	public void setIp(String ip) {
		this.ip = ip;
	}
	
}
